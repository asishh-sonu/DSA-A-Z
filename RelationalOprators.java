public class RelationalOprators{
    public static void main(String args[]){
        int p = 22, q = 30;

        System.out.println(p==q);
        System.out.println(p!=q);
        System.out.println(p>=q);
        System.out.println(p<=q);
        System.out.println(p<q);
        System.out.println(p>q);

    }
}