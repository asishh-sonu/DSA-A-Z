public class Nmm{
    public static int sum(int a, int b){
        return a + b ;
    }
    public static float sum(float a, float b){
        return a + b;
    }
    public static void main(String args[]){
        System.out.println(sum(3,10));
        System.out.println(sum(2.3f,2.0f));
    }
}